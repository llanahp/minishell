#include "../minishell.h"

int	echo(t_command *cmd)
{
	int	i;

	i = 0;
	while (cmd != NULL && cmd->args[i] && i < 4)
	{
		printf("%s\n", cmd->args[i]);
		i++;
	}
	return (0);
}
